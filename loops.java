public class loops {
    public static void main(String[] args) {
        System.out.println("Hello world");
        System.out.println("Hello world");
        System.out.println("Hello world");
        System.out.println("Hello world");
        System.out.println("Hello world");

        // print hello world 5 times
        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello world");          
        }
    }
}

